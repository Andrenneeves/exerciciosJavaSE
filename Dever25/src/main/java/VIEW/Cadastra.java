    private void btn_cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cadastrarActionPerformed
        Pessoa p = new Pessoa();
        p.setNome(txt_nome.getText());
        p.setGenero(txt_genero.getText());
        p.setIdade(txt_idade.getText());
        
        txt_nome.setText("");
        txt_idade.setText("");
        txt_genero.setText("");
                
        FileWriter arquivo;
        try {
            arquivo = new FileWriter("c:\\java\\saidas\\exportar.txt",true);
            PrintWriter print = new PrintWriter(arquivo);
            
            print.println(p.getNome() + ";" + p.getIdade() + ";" + p.getGenero());
            arquivo.close();
            
            JOptionPane.showMessageDialog(rootPane, "Pessoa Cadastrada", "Sucesso", HEIGHT);

        } catch (IOException ex) {
            Logger.getLogger(Cadastra.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btn_cadastrarActionPerformed

